void main() {
  dynamic value = "the easylearn academy";
  print(value);

  value = 12.7;
  print(value);

  value = 3.14;
  print(value);

  value = true;
  print(value);

  //create constant/final variable (whose value can not be changed at all)
  final pi = 3.14; //without type
  print(pi); //3.14

  final String address = "Hill Drive, Bhavnagar";
  print(address);

  const pin = 364001; //without type (int)
  print(pin);

  const bool isCertified = true; //with type
  print(isCertified);

  // isCertified = false;
  // pin = 364009;
  print("Good bye...");
}
